#!/bin/sh

set -feu
# shellcheck disable=SC2296,SC3028,SC3040,SC3054
if [ "${SCRIPT_NAME-}" ]; then
  this_file="${SCRIPT_NAME}"
elif [ "${BASH_SOURCE-}" ]; then
  this_file="${BASH_SOURCE[0]}"
  set -o pipefail
elif [ "${ZSH_VERSION-}" ]; then
  this_file="${(%):-%x}"
  set -o pipefail
else
  this_file="${0}"
fi

case "${STACK+x}" in
  *':'"${this_file}"':'*)
    printf '[STOP]     processing "%s"\n' "${this_file}"
    if (return 0 2>/dev/null); then return; else exit 0; fi ;;
  *) printf '[CONTINUE] processing "%s"\n' "${this_file}" ;;
esac
export STACK="${STACK:-}${this_file}"':'
if [ -n "${INSTALLED_DIR:-}" ]; then
  if [ -d "${INSTALLED_DIR}" ]; then
    echo "Removing ${INSTALLED_DIR}..."
    rm -rf "${INSTALLED_DIR}"
    echo "No local installation directory found for mosquitto at ${INSTALLED_DIR}."
  echo "Uninstalling mosquitto is not supported via this script (or INSTALLED_DIR not set)."
